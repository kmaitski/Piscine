int		ft_count_if(char **tab, int (*f)(char*))
{
	int		x;
	int		y;

	x = 0;
	y = 0;
	while(tab[x])
	{
		if(f(tab[x]) == 1)
			y++;
		x++;
	}
	return (y);
}
