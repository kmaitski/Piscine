char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int x;

	x = 0;
	while (src[x] != '\0' && n  > 0)
	{
		dest[x] = src[x];
		x++;
		n--;
	}
	while (n > 0)
	{
		dest[x] = '\0';
		n--;
		x++;
	}
	return (dest);
}
