char	*ft_strstr(char *str, char *to_find)
{
	int	x;

	x = 0;
	while(str[x] != '\0')
	{
		if (str[x] == to_find[x])
		{
			to_find[x] = str[x];
		}
		x++;
	}
	to_find[x] = '\0';
	return (to_find);
}
