int		ft_strlen(char *str)
{
	int	x;
	int	len;

	x = 0;
	len = 0;
	while (str[x] != '\0')
		x++;
	len = x;
	return (x);
}

int		main()
{
	char str[20]="hey";

	printf("%d", ft_strlen(str));
}
