int		match(char *s1, char *s2)
{
	int		x;
	int		y;

	x = 0;
	y = 0;
	while (s1[x] != '\0')
	{
		if (s2[0] == 42)
		{
			while (s1[x] != '\0' && s1[x] != s2[y])
				x++;
		}
		if (s1[x] != s2[y] && s2[y] != 42)
			return (0);
		while ((s1[x] == s2[y] && s1[x] != '\0') || s2[y] == '\0' || s1[x] != '\0')
		{
			if (s2[y] == 42 && s2[y + 1] == '\0')
				return (1);
			if (s1[x] != s2[y] && s2[x] != 42)
				return (0);
			if (s1[x] != s2[y] && s2[x] == 42)
			{
				while (s1[x] != s2[y])
					x++;
			}
			x++;
		}
	}
	return (1);
}
