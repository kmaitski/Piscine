int		ft_strlen(char *str)
{
	int		x;
	int		y;

	x = 0;
	y = 0;
	while (str[x] != '\0')
	{
		y++;
		x++;
	}
	return (y);
}

int		main(void)
{
	char str[10] = "hello";

	printf("%d", ft_strlen(str));
}
