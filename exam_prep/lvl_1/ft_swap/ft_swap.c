#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int		*temp;

	temp = a;
	a = b;
	b = temp;
	printf("%d", *a);
}

int 	main(void)
{
	int		a;
	int		b;

	a = 4;
	b = 2;
	ft_swap(&a, &b);
}
